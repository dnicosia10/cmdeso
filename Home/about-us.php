<b>WHAT ARE THE FUNCTIONS OF THE CMD-ESO?&nbsp;</b>

<ol>
 <li>&nbsp;</li>
 <ol>
  <li>&nbsp;</li>
  <ol>
   <li>Encourage
       Companys to submit to the CMD-ESO on a regular basis a list of job
       vacancies in their respective establishments in order to facilitate the
       exchange of labor market information services to job seekers and
       Companys by providing employment services to job seeker, both for local
       and overseas employment, and recruitment assistance to Companys;</li>
   <li>Develop
       and administer testing and evaluation instruments for effective job
       selection, training and counseling;</li>
   <li>Provide
       persons with entrepreneurship qualities access to the various livelihood
       and self-employment programs offered by both government and
