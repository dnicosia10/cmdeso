						<br/><br/><b>WHAT IS TESDA?</b><br/><span>The Technical Education and Skills Development Authority (TESDA) was established through the enactment of <a href="http://www.tesda.gov.ph/uploads/File/REPUBLIC%20ACT%20NO.%207796.pdf" target="" rel="">Republic Act No. 7796</a> otherwise known as the "Technical Education and Skills Development Act of 1994", which was signed into law by President Fidel V. Ramos on August 25, 1994. This Act aims to encourage the full participation of and mobilize the industry, labor, local government units and technical-vocational institutions in the skills development of the country's human resources.<br>
<br>
The merging of the National Manpower and Youth Council (NMYC) of the Department of Labor and Employment (DOLE). The Bureau of Technical and Vocational Education (BTVE) of the Department of Education, Culture and Sports (DECS), and The Apprenticeship Program of the Bureau of Local Employment (BLE) of the DOLE gave birth to TESDA.<br>
<br>
The fusion of the above offices was one of the key recommendations of the 1991 Report of the Congressional Commission on Education, which undertook a national review of the state of Philippine education and manpower development. It was meant to reduce overlapping in skills development activities initiated by various public and private sector agencies, and to provide national directions for the country's technical-vocational education and training (TVET) system. Hence, a major thrust of TESDA is the formulation of a comprehensive development plan for middle-level manpower based on the National Technical Education and Skills Development Plan. This plan shall provide for a reformed industry-based training program that includes apprenticeship, dual training system and other similar schemes.<br>
<br>
TESDA is mandated to:</span>

<ol>
	<li>Integrate, coordinate and monitor skills development programs;</li>
	<li>Restructure efforts to promote and develop middle-level manpower;</li>
	<li>Approve skills standards and tests;</li>
	<li>Develop an accreditation system for institutions involved in middle-level manpower development;</li>
	<li>Fund programs and projects for technical education and skills development; and</li>
	<li>Assist trainers training programs.</li>
</ol>

&nbsp;

<span><br>
At the same time, TESDA is expected to:</span>

<ol>
	<li>&nbsp;Devolve training functions to local governments;</li>
	<li>&nbsp;Reform the apprenticeship program;</li>
	<li>Involve industry/Companys in skills training;</li>
	<li>Formulate a skills development plan;</li>
	<li>Develop and administer training incentives;</li>
	<li>Organize skills competitions; and</li>
	<li>Manage skills development funds.</li>
</ol>

<span><br>
Overall, TESDA formulates manpower and skills plans, sets appropriate skills standards and tests, coordinates and monitors manpower policies and programs, and provides policy directions and guidelines for resource allocation for the TVET institutions in both the private and public sectors.<br>
<br>
Today, TESDA has evolved into an organization that is responsive, effective and efficient in delivering myriad services to its clients. To accomplish its multi-pronged mission, the TESDA Board has been formulating strategies and programs geared towards yielding the highest impact on manpower development in various areas, industry sectors and institutions.</span>		
