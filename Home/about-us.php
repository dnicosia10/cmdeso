       employment and occupational counseling, career guidance, mass motivation
       and values development activities;</li>
   <li>Conduct
       pre-employment counseling and orientation to prospective local and
       overseas workers;</li>
   <li>Provide
       reintegration assistance services to returning Filipino migrant workers:
