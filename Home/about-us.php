						<b>WHAT IS CMD-ESO?</b>

The
Public Employment Service Office or CMD-ESO is a non-fee charging multi-employment
service facility or entity established or accredited pursuant to Republic Act
No. 8759 otherwise known as the CMD-ESO Act of 1999.

To carry out full employment and equality of employment
opportunities for all, and for this purpose, to strengthen and expand the
existing employment facilitation service machinery of the government
particularly at the local levels there shall be established in all capital
towns of provinces, key cities, and other strategic areas a Public Employment
Service Office, Hereinafter referred to as CMD-ESO, which shall be community-based
and maintained largely by local government units (LGUs) and a number of
non-governmental organizations (NGOs) or community-based organizations (CBOs)
and state universities and colleges (SUCs). The CMD-ESOs shall be linked to the
regional offices of the Department of Labor and Employment (DOLE) for
coordination and technical supervision, and to the DOLE central office, to
constitute the national employment service network.

